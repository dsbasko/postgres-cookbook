-- schema.sql — DDL-добавки ЭТОГО юнита поверх канона Brew (schema/brew.sql).
--
-- Тема — навигация по соседним строкам (lag/lead) и оконные фреймы (ROWS vs
-- RANGE). Нужен ровный временной ряд «выручка по дням» — и намеренный ПРОПУСК
-- одного дня, чтобы показать, чем ROWS (считает физические строки) отличается
-- от RANGE (считает по значению даты). Канон с тремя заказами для этого не
-- годится — берём свою лабораторную таблицу daily_revenue_lab. Канон не трогаем.
--
-- Идемпотентно: DROP TABLE IF EXISTS + CREATE + фиксированный seed → вывод
-- демо воспроизводится дословно при любом прогоне.

DROP TABLE IF EXISTS daily_revenue_lab;

-- daily_revenue_lab — дневная выручка кофейни в центах. Обрати внимание: 05
-- февраля ПРОПУЩЕНО (выходной) — этот разрыв и разведёт ROWS и RANGE на
-- скользящем среднем.
CREATE TABLE daily_revenue_lab (
    day   date   PRIMARY KEY,
    cents bigint NOT NULL
);

