-- demo.sql — экскурсия по psql как рабочему инструменту (цель `make run`).
--
-- Это escape-hatch-юнит: урок про сам psql, его мета-команды (`\dt`, `\d`, `\x`
-- и т.п.) — это команды клиента, а не SQL, и sqlc их не генерирует. Поэтому
-- урок ведётся psql-скриптом напрямую, без query.sql + кодоген.
--
-- Демо только читает канон Brew (структуру и одну строку меню) — данные не
-- меняет, поэтому идемпотентно и вывод воспроизводится дословно. Часть
-- мета-команд (`\l`, `\timing`, `\i`) в демо не попала намеренно: их вывод
-- зависит от машины (локаль, замер времени) — они разобраны в README прозой.
--
-- Внутри \echo обратный слэш экранируется как \\, иначе psql съедает его как
-- C-эскейп (\d → d). На сами мета-команды ниже это не влияет.

\set ON_ERROR_STOP on

\echo '== \\dt — какие таблицы есть в базе brew (схема Brew: 9 таблиц) =========='
\dt

\echo ''
\echo '== \\d drinks — структура: колонки, типы, PK и кто на таблицу ссылается =='
\d drinks

\echo ''
\echo '== \\x + SELECT — расширенный вывод: одна строка столбиком (для широких) =='
\x on
SELECT id, sku, name, category, base_price, stock FROM drinks WHERE sku = 'CLD-01';
\x off
