-- session-b.sql — СЕССИЯ B (писатель). Запусти во ВТОРОМ терминале по подсказке
-- из session-a:
--   make session-b
--
-- B поднимает цену капучино 450 → 500 и коммитит. Внутри своей транзакции B
-- сразу видит новое значение (own writes). Для сессии A это изменение станет
-- видимым только в новом снапшоте — то есть после её COMMIT (шаг A3), а не в
-- открытой транзакции (шаг A2).

\set ON_ERROR_STOP on

\echo 'Сессия B — писатель.'
BEGIN;

\echo ''
\echo 'B1) Поднимаем цену капучино 450 → 500 (xmin строки теперь = id транзакции B):'
UPDATE drinks SET base_price = 500 WHERE id = 2;
SELECT id, name, base_price, xmin FROM drinks WHERE id = 2;

\echo ''
\echo 'B2) COMMIT — версия B становится видимой для НОВЫХ снапшотов (но не для уже'
\echo '    открытой транзакции A):'
COMMIT;

\echo ''
\echo '>>> Готово. Вернись в терминал A и нажми Enter.'
