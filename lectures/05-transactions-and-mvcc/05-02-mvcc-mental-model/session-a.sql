-- session-a.sql — СЕССИЯ A (читатель). Запусти в ПЕРВОМ терминале:
--   make session-a
--
-- A открывает транзакцию REPEATABLE READ — она фиксирует один снапшот на всю
-- транзакцию. Между A1 и A2 ты в другом терминале прогонишь session-b (писатель
-- меняет цену и коммитит). Суть урока: A2 в той же транзакции всё равно увидит
-- СТАРОЕ значение — снапшот A взят раньше, чем B закоммитил. Это и есть
-- изоляция через MVCC: чтения не блокируют записи, и наоборот.
--
-- Порядок шагов задают подсказки \prompt — они держат транзакцию A открытой,
-- пока ты не вернёшься. Поэтому сценарий детерминирован, а не гонка.

\set ON_ERROR_STOP on

\echo 'Сессия A — читатель. REPEATABLE READ фиксирует снапшот на всю транзакцию.'
BEGIN ISOLATION LEVEL REPEATABLE READ;

\echo ''
\echo 'A1) Первое чтение — снапшот взят (xmin — id транзакции, создавшей версию):'
SELECT id, name, base_price, xmin FROM drinks WHERE id = 2;

\echo ''
\prompt 'Теперь в другом терминале выполни `make session-b`. Когда B напечатает COMMIT — нажми Enter здесь... ' _

\echo ''
\echo 'A2) Второе чтение в ТОЙ ЖЕ транзакции: B уже закоммитил, но снапшот A прежний —'
\echo '    значение и xmin не изменились. A не видит чужой коммит, пришедший после её снапшота.'
SELECT id, name, base_price, xmin FROM drinks WHERE id = 2;

COMMIT;

\echo ''
\echo 'A3) Транзакция A закрыта — следующий запрос берёт НОВЫЙ снапшот. Теперь видно'
\echo '    запись B: и base_price, и xmin другие (это уже версия, созданная транзакцией B).'
SELECT id, name, base_price, xmin FROM drinks WHERE id = 2;

\echo ''
\echo '>>> Демо завершено. Восстанови меню: `make db-reset`.'
