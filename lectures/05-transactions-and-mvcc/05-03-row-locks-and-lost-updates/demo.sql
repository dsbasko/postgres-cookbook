-- demo.sql — детерминированное демо потерянного обновления и блокировок строк
-- (цель `make run`).
--
-- Это escape-hatch-юнит: урок про конкуренцию двух сессий, sqlc тут не помощник.
-- Этот файл — «основной демо»: он воспроизводимо ПОКАЗЫВАЕТ арифметику
-- потерянного обновления (без реальной гонки — два устаревших чтения мы
-- имитируем psql-переменными) и его починку. Реальную конкуренцию двух живых
-- транзакций показывают session-a.sql / session-b.sql (очередь на FOR UPDATE
-- SKIP LOCKED).
--
-- Работаем на отдельном лабораторном столе seat_lab, а DROP в конце оставляет
-- песочницу нетронутой — демо идемпотентно, вывод воспроизводится дословно.

\set ON_ERROR_STOP on
\pset null '∅'
SET client_min_messages = warning;  -- глушим NOTICE от DROP ... IF EXISTS

DROP TABLE IF EXISTS seat_lab;
CREATE TABLE seat_lab (id int PRIMARY KEY, name text, on_hand int NOT NULL);
INSERT INTO seat_lab VALUES (1, 'Колд брю', 10);

-- ── Часть 1. Потерянное обновление (read-modify-write в приложении) ──────────
-- Опасный паттерн: приложение ЧИТАЕТ остаток в переменную, считает новое
-- значение у себя и ПИШЕТ его назад. Два процесса, прочитавшие одно и то же
-- «10», оба запишут «9» — один декремент потерян. Реальную одновременность мы
-- имитируем двумя \gset: оба «воркера» захватывают остаток ДО любой записи.
\echo '── Часть 1. Потерянное обновление: два воркера прочитали остаток ДО записи ──'
SELECT on_hand AS w1 FROM seat_lab WHERE id = 1 \gset
SELECT on_hand AS w2 FROM seat_lab WHERE id = 1 \gset
\echo 'Воркер 1 прочитал остаток =' :w1 '; воркер 2 прочитал остаток =' :w2

-- Каждый воркер пишет «своё прочитанное минус 1». Второй UPDATE затирает первый.
UPDATE seat_lab SET on_hand = :w1 - 1 WHERE id = 1;   -- воркер 1: 10 - 1 = 9
UPDATE seat_lab SET on_hand = :w2 - 1 WHERE id = 1;   -- воркер 2: 10 - 1 = 9 (затёр!)
SELECT on_hand AS "остаток после двух продаж" FROM seat_lab WHERE id = 1;
\echo '→ продали ДВА колд брю, а остаток упал лишь на единицу: один декремент потерян.'

-- ── Часть 2. Атомарный UPDATE — починка по умолчанию ─────────────────────────
-- Если арифметику делает САМА база (on_hand = on_hand - 1), команда читает и
-- пишет под блокировкой строки, которую UPDATE берёт на время своего
-- выполнения. Конкурент ждёт завершения — устаревшего чтения не существует.
UPDATE seat_lab SET on_hand = 10 WHERE id = 1;   -- вернули остаток к 10
\echo ''
\echo '── Часть 2. Атомарный UPDATE: арифметику делает БД, потери нет ──'
UPDATE seat_lab SET on_hand = on_hand - 1 WHERE id = 1;   -- 10 → 9
UPDATE seat_lab SET on_hand = on_hand - 1 WHERE id = 1;   -- 9 → 8
SELECT on_hand AS "остаток после двух продаж" FROM seat_lab WHERE id = 1;
\echo '→ две продажи — остаток 8. Оба декремента на месте.'

-- ── Часть 3. FOR UPDATE — когда read-modify-write неизбежен в приложении ─────
-- Иногда между чтением и записью нужна логика на стороне приложения (проверить
-- лимит, обратиться во внешний сервис). Тогда строку ЯВНО блокируют на чтении:
-- SELECT ... FOR UPDATE держит блокировку до конца транзакции, и конкурент,
-- читающий ту же строку с FOR UPDATE, ЖДЁТ нашего COMMIT — устаревшее чтение
-- снова невозможно. (Само ожидание видно только во второй сессии — см. ниже.)
\echo ''
\echo '── Часть 3. FOR UPDATE: явная блокировка строки на время транзакции ──'
BEGIN;
SELECT id, name, on_hand FROM seat_lab WHERE id = 1 FOR UPDATE;   -- строка залочена
UPDATE seat_lab SET on_hand = on_hand - 1 WHERE id = 1;           -- 8 → 7
COMMIT;
SELECT on_hand AS "остаток после третьей продажи" FROM seat_lab WHERE id = 1;

DROP TABLE seat_lab;
