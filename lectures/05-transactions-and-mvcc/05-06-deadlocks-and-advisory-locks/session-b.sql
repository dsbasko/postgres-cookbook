-- session-b.sql — СЕССИЯ B. Запусти во ВТОРОМ терминале по подсказке из
-- session-a:
--   make session-b
--
-- B блокирует строки во встречном к A порядке: сначала #2, потом #1. Именно
-- этот «крест» (A: #1→#2, B: #2→#1) и создаёт дедлок. Второй UPDATE в B
-- замыкает цикл — после него Postgres обнаруживает взаимную блокировку.

\set ON_ERROR_STOP off

\echo 'Сессия B. Открываем транзакцию и берём строку #2 (встречный к A порядок).'
BEGIN;

\echo ''
\echo 'B1) Берём (блокируем) строку #2:'
UPDATE lock_lab SET v = v + 1 WHERE id = 2;

\echo ''
\prompt 'Теперь вернись в терминал A и нажми Enter — A зависнет, потянувшись за #2. Затем вернись СЮДА и нажми Enter... ' _

\echo ''
\echo 'B2) Тянемся за строкой #1 (её держит A) → цикл замыкается, Postgres ловит дедлок:'
UPDATE lock_lab SET v = v + 1 WHERE id = 1;   -- замыкает цикл → 40P01 одной из транзакций
COMMIT;

\echo ''
\echo '>>> Одна из транзакций (A или B) получила ERROR 40P01 и откатилась, вторая прошла.'
\echo '    Вернись в терминал A.'
