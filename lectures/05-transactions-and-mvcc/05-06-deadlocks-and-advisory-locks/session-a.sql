-- session-a.sql — СЕССИЯ A. Запусти в ПЕРВОМ терминале:
--   make session-a
--
-- A и B блокируют две строки во ВСТРЕЧНОМ порядке: A берёт #1 потом #2, B берёт
-- #2 потом #1. Каждая ждёт строку, которую держит другая, — образуется цикл, и
-- ни одна не может продолжить. Это дедлок. Postgres обнаруживает его сам (через
-- deadlock_timeout, по умолчанию 1с) и разрывает: выбирает «жертву» и завершает
-- её транзакцию ошибкой 40P01, вторая транзакция проходит.
--
-- ON_ERROR_STOP off: 40P01 мы хотим УВИДЕТЬ, а не молча упасть. Порядок шагов
-- держит \prompt, но какую транзакцию назначить жертвой — решает Postgres.

\set ON_ERROR_STOP off
SET client_min_messages = warning;

\echo 'Сессия A. Готовим стол из двух строк и открываем транзакцию.'
DROP TABLE IF EXISTS lock_lab;
CREATE TABLE lock_lab (id int PRIMARY KEY, name text, v int NOT NULL);
INSERT INTO lock_lab VALUES (1, 'строка #1', 0), (2, 'строка #2', 0);

BEGIN;

\echo ''
\echo 'A1) Берём (блокируем) строку #1:'
UPDATE lock_lab SET v = v + 1 WHERE id = 1;

\echo ''
\prompt 'Теперь в другом терминале запусти `make session-b` — он возьмёт строку #2 и встанет у подсказки. Когда B будет готов — нажми Enter здесь... ' _

\echo ''
\echo 'A2) Тянемся за строкой #2 (её держит B) → A ЗАВИСАЕТ в ожидании.'
\echo '    Как только B потянется за строкой #1, цикл замкнётся, и Postgres разорвёт дедлок:'
UPDATE lock_lab SET v = v + 1 WHERE id = 2;   -- блокируется; при дедлоке вернёт 40P01
COMMIT;

\echo ''
\echo 'A3) Если выше ERROR 40P01 — жертвой выбрали A, её транзакция откачена целиком.'
\echo '    Если A прошла и закоммитила — жертвой стала B. Жертву выбирает Postgres.'
\echo '>>> Прибраться: make db-reset (таблицу lock_lab можно DROP вручную).'
