-- session-b.sql — СЕССИЯ B. Запусти во ВТОРОМ терминале по подсказке из
-- session-a:
--   make session-b
--
-- B — второй бариста (Борис). Он тоже под SERIALIZABLE читает «на полу двое» и
-- снимает СВОЙ флаг (id=2). B коммитит ПЕРВЫМ — и его транзакция проходит
-- успешно. Конфликт обнаружится у того, кто коммитит вторым (это сессия A): её
-- COMMIT упадёт с 40001. То есть «виноват» не тот, кто ошибся, а тот, кто
-- закрыл опасную пару зависимостей последним.

\set ON_ERROR_STOP on

\echo 'Сессия B — бариста Борис. Открываем транзакцию SERIALIZABLE.'
BEGIN ISOLATION LEVEL SERIALIZABLE;

\echo ''
\echo 'B1) Борис смотрит, сколько на полу (видит 2 ≥ 1 → уйти можно):'
SELECT count(*) AS "на полу" FROM shift_lab WHERE on_floor;

\echo ''
\echo 'B2) Борис снимает СВОЙ флаг (id=2) и коммитит ПЕРВЫМ — успешно:'
UPDATE shift_lab SET on_floor = false WHERE id = 2;
COMMIT;

\echo ''
\echo '>>> B закоммитил без ошибки. Теперь вернись в терминал A и нажми Enter —'
\echo '    COMMIT сессии A упадёт с 40001 (A — второй коммитер).'
